package model;

import java.util.Date;

/**
 *
 * @author HP
 */
public class PriceHistory {
    private Integer id;
    private Date date;
    private Float price;
    private User user;
    private Product product;

    public PriceHistory() {
    }

    public PriceHistory(Integer id, Date date, Float price, User user, Product product) {
        this.id = id;
        this.date = date;
        this.price = price;
        this.user = user;
        this.product = product;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public Float getPrice() {
        return price;
    }

    public void setPrice(Float price) {
        this.price = price;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Product getProduct() {
        return product;
    }

    public void setProduct(Product product) {
        this.product = product;
    }
    
    
}
