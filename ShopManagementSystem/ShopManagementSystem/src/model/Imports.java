package model;

import java.util.Date;

/**
 *
 * @author HP
 */
public class Imports {
    private Integer id;

    private Date date;

    private float totalPrice;

    private User user;

    public Imports() {
    }

    public Imports(Integer id, Date date, float totalPrice, User user) {
        this.id = id;
        this.date = date;
        this.totalPrice = totalPrice;
        this.user = user;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public float getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(float totalPrice) {
        this.totalPrice = totalPrice;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
    
}
