    private void jButton_ChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ChooseActionPerformed
        // TODO add your handling code here:
        isEdit = false;
        int selectedRow = jTable_Product.convertRowIndexToModel(jTable_Product.getSelectedRow());
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "There is no selected product");
            return;
        }
        
        int id = Integer.parseInt(jTable_Product.getValueAt(selectedRow, 0).toString());
        
        for(int i = 0; i < importDetail.size(); i++){
            if(importDetail.get(i).getProduct().getProductId() == id){
                JOptionPane.showMessageDialog(this, "This item has been already added to Import Detail");
                return;
            }
        }
        
        productImportDialog = new ProductImportDialog(null, true, this, 
                jTable_Product.getValueAt(selectedRow, 0).toString(), 
                jTable_Product.getValueAt(selectedRow, 1).toString(), 
                Integer.parseInt(jTable_Product.getValueAt(selectedRow, 6).toString()), 
                Float.parseFloat(jTable_Product.getValueAt(selectedRow, 5).toString()));

        productImportDialog.setVisible(true);
        System.out.println(totalPrice);
        jLabel_Total.setText(String.valueOf(totalPrice));
        loadImportDetail();
    }//GEN-LAST:event_jButton_ChooseActionPerformed

