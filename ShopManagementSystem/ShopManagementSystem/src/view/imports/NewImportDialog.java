    private void jButton_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_DeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable_ImportDetail.convertRowIndexToModel(jTable_ImportDetail.getSelectedRow());
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "There is no selected import detail");
            return;
        }
        
        for(int i = 0; i < importDetail.size(); i++){
            if(importDetail.get(i).getProduct().getProductId() == Integer.parseInt(jTable_ImportDetail.getValueAt(selectedRow, 0).toString())){
                totalPrice -= importDetail.get(i).getPrice()*importDetail.get(i).getQuantity();
                importDetail.remove(i);
            }
        }
        System.out.println(totalPrice);
        jLabel_Total.setText(String.valueOf(totalPrice));
        loadImportDetail();
    }//GEN-LAST:event_jButton_DeleteActionPerformed

