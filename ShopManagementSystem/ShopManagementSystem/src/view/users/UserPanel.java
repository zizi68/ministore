    private void jTable_ShipperMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_ShipperMouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable_Shipper.convertRowIndexToModel(jTable_Shipper.getSelectedRow());
        if (selectedRow == -1) {
            return;
        }
        int id = Integer.parseInt(jTable_Shipper.getValueAt(selectedRow, 0).toString());
        
        UserDB u = null;
        for (UserDB f : listShipper){
            if(f.getId() == id)
                u = f;
        }

        jTextField_ID1.setText(u.getId() + "");
        jTextField_Username1.setText(u.getUsername());
        jTextField_LastName1.setText(u.getLastName());
        jTextField_FirstName1.setText(u.getFirstName());
        jTextField_Email1.setText(u.getEmail());
        jTextField_Phone1.setText(u.getPhone());
        imageName = u.getImage();
        Image img = uc.getImage(u.getImage());
        Image newImg = img.getScaledInstance(jLabel_Image1.getWidth(), jLabel_Image1.getHeight(), java.awt.Image.SCALE_SMOOTH);
        ImageIcon icon = new ImageIcon(newImg);
        jLabel_Image1.setIcon(icon);
        jButton_Modify1.setEnabled(true);
        jButton_Remove1.setEnabled(true);
    }//GEN-LAST:event_jTable_ShipperMouseClicked

