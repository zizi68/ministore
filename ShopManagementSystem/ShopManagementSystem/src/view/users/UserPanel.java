    public void loadShipper(){
        listShipper = uc.getUserByRole("shipper");
        Vector vt;
        dtmShipper.setNumRows(0);
        for (UserDB f : listShipper) {
            vt = new Vector();
            vt.add(f.getId());
            vt.add(f.getUsername());
            vt.add(f.getLastName());
            vt.add(f.getFirstName());
            vt.add(f.getEmail());
            vt.add(f.getPhone());
            dtmShipper.addRow(vt);
        }
    }
    
