    private void jButton_Remove1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Remove1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable_Shipper.convertRowIndexToModel(jTable_Shipper.getSelectedRow());
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "There is no selected product");
            return;
        }

        int luaChon = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this shipper?");
        if (luaChon == JOptionPane.CANCEL_OPTION) {
            return;
        } else if (luaChon == JOptionPane.OK_OPTION) {
            User u = new User();
            u.setId(Integer.parseInt(jTextField_ID.getText()));
            Response response = uc.updateStatusByID(u);
            JOptionPane.showMessageDialog(this, uc.convertResponse(response.getMessage()).getMessage());
            if (response.getResponseCode() == 200) {
                loadShipper();
                clearAll();
            } else {
                return;
            }
        }
    }//GEN-LAST:event_jButton_Remove1ActionPerformed

    private void jButton_OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_OKActionPerformed
        // TODO add your handling code here:
        String id = jTextField_ID1.getText();
        String username = jTextField_Username1.getText();
        String firstName = jTextField_FirstName1.getText();
        String lastName = jTextField_LastName1.getText();
        String email = jTextField_Email1.getText();
        String phone = jTextField_Phone1.getText();

        if (mode == Mode.ADD) {
            UserDB user = new UserDB();
            user.setUsername(username);
            user.setFirstName(firstName);
            user.setLastName(lastName);
            user.setEmail(email);
            user.setPhone(phone);
            user.setPassword(username);
            user.setStatus(true);
            Set<String> role = new HashSet<>();
            role.add("shipper");
            user.setRole(role);
            if (selectedFile != null) {
                try {
                    RequestBody requestBody = RequestBody.create(MediaType.parse("multipart/form-data"), selectedFile);
                    MultipartBody.Part part = MultipartBody.Part.createFormData("file", selectedFile.getName(), requestBody);
                    UploadFileService uploadFileInterface = APIClient.getClient().create(UploadFileService.class);
                    uploadFileInterface.uploadUserImage(part).enqueue(new Callback<ResponseBody>() {

                        @Override
                        public void onResponse(Call<ResponseBody> call, retrofit2.Response<ResponseBody> response) {
                            try {
                                if (response.isSuccessful()) {
                                    String str = response.body().string();
                                    user.setImage(str);
                                    System.out.println("vi: " + user.getImage());
                                    Response res = uc.addShipper(user);
                                    JOptionPane.showMessageDialog(null, uc.convertResponse(res.getMessage()).getMessage());
                                    if (res.getResponseCode() == 200) {
                                        loadShipper();
                                        mode = Mode.FREE;
                                        UIController.showCardLayout("cardFirst", jPanel_Card);
                                        setEditableForAll(false);
                                        jTable_Shipper.setEnabled(true);
                                        selectedFile = null;
                                    } else {
                                        return;
                                    }
                                }
                            } catch (Exception e) {
                                JOptionPane.showMessageDialog(null, e.getMessage());
                            }
                        }

                        @Override
                        public void onFailure(Call<ResponseBody> call, Throwable t) {
                            JOptionPane.showMessageDialog(null, t.getMessage());
                        }

                    });
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            } else {
                user.setImage(imageName);
                Response response = uc.addShipper(user);
                JOptionPane.showMessageDialog(this, uc.convertResponse(response.getMessage()).getMessage());
                if (response.getResponseCode() == 200) {
                    loadShipper();
                    mode = Mode.FREE;
                    UIController.showCardLayout("cardFirst", jPanel_Card);
                    setEditableForAll(false);
                    jTable_Shipper.setEnabled(true);
                    selectedFile = null;
                } else {
                    return;
                }
            }
        }
        if (mode == Mode.MODIFY) {
            UserDB user = new UserDB();
            user.setId(Long.parseLong(id));
            user.setUsername(username);
            user.setFirstName(firstName);
            user.setLastName(lastName);
            user.setEmail(email);
            user.setPhone(phone);
            if (selectedFile != null) {
                try {
                    RequestBody requestBody = RequestBody.create(MediaType.parse("multipart/form-data"), selectedFile);
                    MultipartBody.Part part = MultipartBody.Part.createFormData("file", selectedFile.getName(), requestBody);
                    UploadFileService uploadFileInterface = APIClient.getClient().create(UploadFileService.class);
                    uploadFileInterface.uploadProductImage(part).enqueue(new Callback<ResponseBody>() {

                        @Override
                        public void onResponse(Call<ResponseBody> call, retrofit2.Response<ResponseBody> response) {
                            try {
                                if (response.isSuccessful()) {
                                    String str = response.body().string();
                                    user.setImage(str);
                                    System.out.println("vi: " + user.getImage());
                                    Response res = uc.updateUser(user);
                                    JOptionPane.showMessageDialog(null, uc.convertResponse(res.getMessage()).getMessage());
                                    if (res.getResponseCode() == 200) {
                                        loadShipper();
                                        mode = Mode.FREE;
                                        UIController.showCardLayout("cardFirst", jPanel_Card);
                                        setEditableForAll(false);
                                        jTable_Shipper.setEnabled(true);
                                        selectedFile = null;
                                    } else {
                                        return;
                                    }
                                }
                            } catch (Exception e) {
                                JOptionPane.showMessageDialog(null, e.getMessage());
                            }
                        }

                        @Override
                        public void onFailure(Call<ResponseBody> call, Throwable t) {
                            JOptionPane.showMessageDialog(null, t.getMessage());
                        }

                    });
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            } else {
                user.setImage(imageName);
                Response response = uc.updateUser(user);
                JOptionPane.showMessageDialog(this, uc.convertResponse(response.getMessage()).getMessage());
                if (response.getResponseCode() == 200) {
                    loadShipper();
                    mode = Mode.FREE;
                    UIController.showCardLayout("cardFirst", jPanel_Card);
                    setEditableForAll(false);
                    jTable_Shipper.setEnabled(true);
                    selectedFile = null;
                } else {
                    return;
                }
            }
        }

    }//GEN-LAST:event_jButton_OKActionPerformed

